void main() {
  
  int num1= 20;
  int num2 = 43;

  int sum = num1 + num2;
  int reduce = num2 - num1;
  int multi = num1 * num2;
  double divide = num2 / num1;
  int divide2 = num2 ~/ num1;
  int modulo = num2 % num1;

  print(sum);
  print(reduce);
  print(multi);
  print(divide);
  print(divide2);
  print(modulo);
  print(++num1);
  print(num1);
  print(--num2);
  print(num2);
}

